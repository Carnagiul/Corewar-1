#include "asm.h"

int			skip_space(char **s)
{
	int		len;

	len = 1;
	while ((**s) == ' ' || (**s) == '\t')
	{
		len++;
		(*s)++;
	}
	if (**s == '\0')
		return (0);
	return (len);
}

int			skip_space_len(char **s)
{
	int		len;

	len = 0;
	while ((**s) == ' ' || (**s) == '\t')
	{
		len++;
		(*s)++;
	}
	return (len);
}
